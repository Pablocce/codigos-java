package examen;


public class modulo {
	private String nombre;
	private int horas;
	
	
	public modulo(String nombre, int horas) {
		super();
		this.nombre = nombre;
		this.horas = horas;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public void setPoblacion(int horas) {
		this.horas = horas;
	}
	
	public String getNombre() {
		return this.nombre;
	}
	
	public int getHoras() {
		return this.horas;
	}
	
	
	
	
	
}
